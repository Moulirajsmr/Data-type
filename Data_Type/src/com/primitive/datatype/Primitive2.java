package com.primitive.datatype;

public class Primitive2 {
	
	public static void main(String[] args) {
		
		byte m = 90;
		System.out.println("Age = " + m);
		short n = 1900 ;
		System.out.println("Birth year = " + n);
		int o = 893567429 ;
		System.out.println("Phone number = " + o);
		long p = 9548985496436l ;
		System.out.println("Aadhar number = " + p);
		char q = 'M';
		System.out.println("Gender = " +q);
		boolean r = true ;
		System.out.println("Citizen of India = " + r);
		float s = 0.156454f;
		System.out.println("Float value = " + s);
		double t = 0.5498545894984968;
		System.out.println("Double value = " + t);
	}

}
